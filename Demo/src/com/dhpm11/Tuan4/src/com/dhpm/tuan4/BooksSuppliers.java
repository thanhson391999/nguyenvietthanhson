    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        LuuGiaTri L = new LuuGiaTri();
        L.setName(txtName.getText());
        L.setAddress(txtAddress.getText());
        L.setContact(txtContact.getText());
        L.setEmail(txtEmail.getText());
        L.setRemarks(txtaremarks.getText());
        String tam = "";
        if (cbBooks.isSelected())
        {
            tam = tam + "Books |";
        }
        if (cbJournalsandmagazines.isSelected())
        {
            tam = tam + "Journals and magazines |";
        }
        if (cbNewpapers.isSelected())
        {
            tam = tam + "New papers |";
        }
        L.setType(tam);
        
        if (new KetNoi().addData(L))
        {
            JOptionPane.showMessageDialog(rootPane, "luu thanh cong !");
            list.add(L);
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, "Luu that bai !");
        }
            showResult();
    }//GEN-LAST:event_btnSaveActionPerformed

