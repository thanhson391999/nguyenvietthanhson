   public  void LoadData(){
        tblxuathien.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
        if(tblxuathien.getSelectedRow() >=0){
            txtId.setText(tblxuathien.getValueAt(tblxuathien.getSelectedRow(),0) +"");
            txtName.setText(tblxuathien.getValueAt(tblxuathien.getSelectedRow(),1) +"");
            txtAddress.setText(tblxuathien.getValueAt(tblxuathien.getSelectedRow(),3) +"");
            txtContact.setText(tblxuathien.getValueAt(tblxuathien.getSelectedRow(),4) +"");
            txtEmail.setText(tblxuathien.getValueAt(tblxuathien.getSelectedRow(),5) +"");
            txtaremarks.setText(tblxuathien.getValueAt(tblxuathien.getSelectedRow(),6) +"");
                }        
             }
        });
   }
    public void showResult(){
        LuuGiaTri L = list.get(list.size() -1);
         model.addRow(new Object[]{
                 L.getID(), L.getName(), L.getType(), L.getAddress(), L.getContact(), L.getEmail(), L.getRemarks()
            });
    }
